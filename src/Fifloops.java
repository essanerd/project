public class Fifloops {
    public static void main(String[] args) {


        for (int i = 0 ; i < 5; i++ ){
        System.out.println( "helo world");
        }


        for (int i =1; i <= 5; i++ )
            System.out.println( "helo world" + i);


        for (int i =0; i < 5; i++ )
            System.out.println( "helo world" + i);


        for (int i =5; i>0 ; i-- )
            System.out.println( "helo world" + i);
    }
}