public class Evennumbers {
    public static void main(String[] args) {

        int[] nums = new int[]{0, 1, 2, 3, 4, 25, 6, 8, 10};
        for (int i = 0; i < nums.length; i++) {
            if (nums[i] % 2 == 0) {
                System.out.println(nums[i]);
            }
        }
    }
}








        






