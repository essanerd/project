import java.util.Scanner;

public class Whileloops {
    public static void main(String[] args) {
        for (int i = 5; i > 0; i--) {
            System.out.println("hello world" + i);

            int iz = 0;
            while (iz > 0) {
                System.out.println("hello world" + iz);
                iz--;







            }
        }
    }
}